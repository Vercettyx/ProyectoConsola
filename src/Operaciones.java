public class Operaciones {
    public static void main(String[] args) {
        int x1=2;
        int x2=4;
        int resultado;
        System.out.println("Suma");
        resultado=x1+x2;
        System.out.println(resultado);
        System.out.println("Resta");
        resultado=x2-x1;
        System.out.println(resultado);
        System.out.println("Multiplicacion");
        resultado=x1*x2;
        System.out.println(resultado);
        System.out.println("division");
        resultado=x2/x1;
        System.out.println(resultado);
    }
}
